export interface ModalConfig{

    /*
        the back drop of the overlay .if set to false the overlay
        will not be droped when click 
    */
    backDrop?:boolean
    /*
        the modal width with persentage 
    */
    modalWidth?:number
    /*
        align centre 
    */
   centre ?:boolean
    /*
       opacity of the overlay percentage
    */
   opacity ?:number
    /*
       Overlay color with Hexa
    */
   backGroundColor ?:string
  
}

